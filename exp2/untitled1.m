% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
 N      = str2num(get(handles.edit1,'String'))%序列长度和采样频率
 fs     = str2num(get(handles.edit2,'String'))
 f1 = str2num(get(handles.edit3,'String'))
 f2 = str2num(get(handles.edit4,'String'))
%%
%生成随机信号样本
%N=1024;fs=1000;                     %序列长度和采样频率
t=(0:N-1)/fs;                       %时间序列
fai=random('unif',0,1,1,2)*2*pi;    %产生2个[0，2pi]内均匀随机数
xn=cos(2*pi*f1*t+fai(1))+3*cos(2*pi*f2*t+fai(2))+randn(1,N);
                                    %产生含噪声的随机序列
                                    %%
%显示数据 plot(handles.wave,t,x);                    
plot(handles.axes3,t,xn);
title(handles.axes3,'随机信号时域波形')
%%
%直接法谱估计
Sx1=abs(fft(xn)).^2/N;              %估计功率谱
f=(0:N/2-1)*fs/N;                   %频率轴坐标
plot(handles.axes1,f,10*log10(Sx1(1:N/2)));grid on; %用dB/Hz做功率谱单位，画图
xlabel('f（Hz）');
ylabel('Sx1(f)(dB/Hz)');
title(handles.axes1,'周期图法估计功率谱');
%%
%与内置函数对比
Sx2=periodogram(xn);
plot(handles.axes2,f,10*log10(Sx2(1:N/2)));grid on;
xlabel('f(Hz)');
ylabel('Sx2(f)(dB/Hz)');
title(handles.axes2,'periodogram函数估计功率谱');





